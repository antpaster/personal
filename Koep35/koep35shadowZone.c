/*! Функция ограничения углов обзора КОЭП
 * Значения углов в радианах
 **************************************************************************************************/
void koep35checkVisionAngles(
    double *Y, /*!< азимут в ССК */
    double *Z, /*!< угол места в ССК */
    uint8_t *limY, /*!< 0 - допустимое значение по Y, 1 - недопустимое значение по Y */
    uint8_t *limZ, /*!< 0 - допустимое значение по Z, 1 - недопустимое значение по Z */
    uint8_t *shadow1deg, /*!< признак затенения 1 град. */
    uint8_t *shadow5deg  /*!< признак затенения 5 град. */
) {
    /* Локальные копии входных параметров */
    uint8_t limYlocal = 0, limZlocal = 0;
    uint8_t shadow1degLocal = 0, shadow5degLocal = 0;
    double yLocal = ( *Y) * RadToGr, zLocal = ( *Z) * RadToGr;

    double actualMinY;
    double actualMaxY;

    /* Определение границ значений углов азимута */
    if( yLocal < cMinY)
    {
        actualMinY = actualMaxY = cMinY; // todo: ?
    }
    else if( yLocal >= cMinY && yLocal < 0.0)
    {
        actualMinY = cMinY;
        actualMaxY = 0.0;
    }
    else if( yLocal >= 0.0 && yLocal < cMaxY)
    {
        actualMinY = 0.0;
        actualMaxY = cMaxY;
    }
    else
    {
        actualMinY = actualMaxY = cMaxY; // todo: ?
    }

    /* Проверка углов на функциональные ограничения обзора КОЭП */
    if( yLocal < actualMinY)
    {
        limYlocal = 1;
        yLocal = actualMinY;
    }
    else if( yLocal >= actualMaxY)
    {
        limYlocal = 1;
        yLocal = actualMaxY;
    }

    if( zLocal <= cMinZ)
    {
        limZlocal = 1;
        zLocal = cMinZ;
    }
    else
    {
        double yValArr[ NODE_POINT_COUNT_KOEP];

        /* Определение массива углов азимута в зависимости от четности подвесок */
        size_t i;
        if( cMinY == actualMinY)
        {
            /* Нечетные подвески */
            for( i = 0; i < NODE_POINT_COUNT_KOEP; ++i)
            {
                yValArr[ i] = ( double)i - 150.0;
            }
        }
        else if( 0.0 == actualMinY)
        {
            /* Четные подвески */
            for( i = 0; i < NODE_POINT_COUNT_KOEP; ++i)
            {
                yValArr[ i] = ( double)i;
            }
        }

        /* Нахождение индекса целого значения азимута (  аргумента), начиная с которого будет
         * осуществляться линейная интерполяция */
        size_t iYvalWholePart = 150;
        for( i = 0; i < NODE_POINT_COUNT_KOEP - 1; ++i)
        {
            if( ( yValArr[ i] >= yLocal) && ( yLocal < yValArr[ i + 1]))
            {
                iYvalWholePart = i;
                break;
            }
        }

        /* Обнуление информации по теням */
        memset( hangPointFlags, 0, HANG_POINT_SHADOW_ALL * sizeof( uint8_t));

        /* Заполнение теней силуэта самолета */
        hangPointFlags[ PLANE_SHAPE_Y_NEG] = hangPointFlags[ PLANE_SHAPE_Y_POS] = 1;

        /* Заполнение теней пусковых установок */
        hangPointFlags[ TP4_RVV_LD] = hangPointFlags[ TP1_KAB_RVP_RVV_BD]
            = hangPointFlags[ TP4_KAB_RVP_LD] = hangPointFlags[ TP9_RVV_LD]
            = hangPointFlags[ TP4_UNCONTROL_AB_NAR_LD] = hangPointFlags[ TP6_RVV_LD]
            = hangPointFlags[ TP9_KAB_RVP_RVV_BD_LD] = hangPointFlags[ TP9_UNCONTROL_AB_LD]
            = hangPointFlags[ TP12_KAB_RVP_RVV_BD_LD] = hangPointFlags[ TP12_UNCONTROL_AB_NAR_LD]
            = 1;

        /* Заполнение теней подвешенного в данный момент оружия */
        assignHangPointFlags( IkarBackTp, 16);

        /* Формула линейной интерполяции на найденном участке */
        double limitZ;
        ThangPointShadow iHps;
        if( 150 != iYvalWholePart)
        {
            /* Не дошли до последнего значения азимута */
            double currZarr[ 2][ cOneBoardSideShadowsCount]; /* массивы текущих значений угла места
                для вычислений */

            /* Предварительная инициализация массивов максимально допустимыми значениями угла
             * места */
            for( i = 0; i < cOneBoardSideShadowsCount; ++i)
            {
                currZarr[ 0][ i] = currZarr[ 1][ i] = 50.0;
            }

            if( cMinY == actualMinY)
            {
                /* Нечетные подвески */
                for( iHps = PLANE_SHAPE_Y_NEG; iHps < HANG_POINT_SHADOW_ALL; iHps += 2)
                {
                    if( hangPointFlags[ iHps])
                    {
                        currZarr[ 0][ iHps / 2] = zLimArrKoep[ iHps][ iYvalWholePart + 1];
                        currZarr[ 1][ iHps / 2] = zLimArrKoep[ iHps][ iYvalWholePart];
                    }
                }
            }
            else if( 0.0 == actualMinY)
            {
                /* Четные подвески */
                for( iHps = PLANE_SHAPE_Y_POS; iHps < HANG_POINT_SHADOW_ALL; iHps += 2)
                {
                    if( hangPointFlags[ iHps])
                    {
                        currZarr[ 0][ iHps / 2] = zLimArrKoep[ iHps][ iYvalWholePart + 1];
                        currZarr[ 1][ iHps / 2] = zLimArrKoep[ iHps][ iYvalWholePart];
                    }
                }
            }

            limitZ = ( minVal( currZarr[ 0], cOneBoardSideShadowsCount)
                    - minVal( currZarr[ 1], cOneBoardSideShadowsCount))
                    * ( yLocal - yValArr[ iYvalWholePart]) + minVal( currZarr[ 1],
                    cOneBoardSideShadowsCount);
        }
        else
        {
            /* Самое большое значение азимута */
            double borderYvalArr[ cOneBoardSideShadowsCount]; /* массив текущих значений угла места
                для вычислений */

            /* Предварительная инициализация массива максимально допустимыми значениями угла
             * места */
            for( i = 0; i < cOneBoardSideShadowsCount; ++i)
            {
                borderYvalArr[ i] = 50.0;
            }

            if( cMinY == actualMinY)
            {
                /* Нечетные подвески */
                for( iHps = PLANE_SHAPE_Y_NEG; iHps < HANG_POINT_SHADOW_ALL; iHps += 2)
                {
                    if( hangPointFlags[ iHps])
                    {
                        borderYvalArr[ iHps / 2] = zLimArrKoep[ iHps][ 150];
                    }
                }
            }
            else if( 0.0 == actualMinY)
            {
                /* Четные подвески */
                for( iHps = PLANE_SHAPE_Y_POS; iHps < HANG_POINT_SHADOW_ALL; iHps += 2)
                {
                    if( hangPointFlags[ iHps])
                    {
                        borderYvalArr[ iHps / 2] = zLimArrKoep[ iHps][ 150];
                    }
                }
            }

            limitZ = minVal( borderYvalArr, cOneBoardSideShadowsCount);
        }

        //        double limitZ = arrValLinearInterpol( yLocal, yValArr, cZlimArrKoep[ iCurrShape],
        //            NODE_POINT_COUNT_KOEP);

        if( zLocal >= limitZ)
        {
            limZlocal = 1;
            shadow1degLocal = 1;
            shadow5degLocal = 1;
            zLocal = limitZ;
        }
        else if( zLocal >= limitZ + c1degInRad)
        {
            shadow1degLocal = 1;
            shadow5degLocal = 1;
        }
        else if( zLocal >= limitZ + c5degInRad)
        {
            shadow5degLocal = 1;
        }
    }

    *Y = yLocal * GrToRad;
    *Z = zLocal * GrToRad;
    *limY = limYlocal;
    *limZ = limZlocal;
    *shadow1deg = shadow1degLocal;
    *shadow5deg = shadow5degLocal;
}
